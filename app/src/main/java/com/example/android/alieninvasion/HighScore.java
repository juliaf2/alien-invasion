/**
 * A high score contains a username and score
 */
public class HighScore {
    public String username;
    public String score;

    /**
     * A simple constructor for a high score
     * @param username - the username to set the username to
     * @param score - the score to set the score to
     */
    public HighScore(String username, String score) {
        this.username = username;
        this.score = score;
    }

    /**
     * Empty constructor for a high score
     */
    public HighScore() {

    }
}
